    void addToTable(String month, Color color) {

        if (color.equals(new Color(231, 76, 60))) {
            
            Date d = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY");

            TableModel model = this.pmf.jTable3.getModel();
            DefaultTableModel dtm = (DefaultTableModel) model;
            
            Vector<String> v = new Vector<String>();
            v.add(String.valueOf(++this.pmf.count));
            v.add(this.pmf.jTable2.getValueAt(row, 0).toString());
            v.add(this.pmf.jTable2.getValueAt(row, 1).toString());
            v.add(sdf.format(d)+" "+month);
            v.add(this.pmf.jTable2.getValueAt(row, 2).toString());
            
            dtm.addRow(v);
            
            this.pmf.tot = this.pmf.tot + Double.parseDouble(this.pmf.jTable2.getValueAt(row, 2).toString());
            
            this.pmf.jLabel14.setText(String.valueOf(this.pmf.tot));
            
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Already paid this month", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }
    
    
    
