    public SelectPayingMonth(PayMonthlyFee pmf, String classId, int row, String theme) {
        this.pmf = pmf;
        this.row = row;
        initComponents();
        themeLoad(theme);
        LocalDate currentDate = LocalDate.now();
        int year = currentDate.getYear();

        String query1 = "SELECT month from student_class_fees \n"
                + "\n"
                + "	INNER JOIN fees\n"
                + "	INNER JOIN student_class\n"
                + "	INNER JOIN month\n"
                + "	\n"
                + "WHERE \n"
                + "	student_class_fees.fees_id = fees.id\n"
                + "	&& student_class_fees.student_class_id = student_class.id\n"
                + "	&& month.id = student_class_fees.month_id\n"
                + "	\n"
                + "	&& fees.student_id = '" + this.pmf.jTextField1.getText() + "'\n"
                + "	&& student_class.class_id = '" + classId + "'\n"
                + "	&& YEAR(fees.date) = '" + year + "'";

        ResultSet rs = search(query1);
        Color c = new Color(39, 174, 96);
        try {
            while (rs.next()) {

                if (rs.getString("month").equals("January")) {
                    jLabel2.setBackground(c);
                }
                if (rs.getString("month").equals("February")) {
                    jLabel4.setBackground(c);
                }
                if (rs.getString("month").equals("March")) {
                    jLabel5.setBackground(c);
                }
                if (rs.getString("month").equals("April")) {
                    jLabel17.setBackground(c);
                }
                if (rs.getString("month").equals("May")) {
                    jLabel18.setBackground(c);
                }
                if (rs.getString("month").equals("June")) {
                    jLabel26.setBackground(c);
                }
                if (rs.getString("month").equals("July")) {
                    jLabel29.setBackground(c);
                }
                if (rs.getString("month").equals("August")) {
                    jLabel21.setBackground(c);
                }
                if (rs.getString("month").equals("September")) {
                    jLabel23.setBackground(c);
                }
                if (rs.getString("month").equals("October")) {
                    jLabel27.setBackground(c);
                }
                if (rs.getString("month").equals("November")) {
                    jLabel28.setBackground(c);
                }
                if (rs.getString("month").equals("December")) {
                    jLabel22.setBackground(c);
                }

            }
        } catch (SQLException ex) {

            ex.printStackTrace();
            Log.warning(ex);
        }

    }

