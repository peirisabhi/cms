    private void jTextField8KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyReleased

        if (!jTextField8.getText().equals("")) {
            if (Double.parseDouble(jTextField8.getText()) >= Double.parseDouble(jLabel14.getText())) {
                jLabel12.setText(String.valueOf(Double.parseDouble(jTextField8.getText()) - Double.parseDouble(jLabel14.getText())));
            } else {
                jLabel12.setText("");
            }
        }

        if (evt.getKeyCode() == 10) {
            if (!jTextField8.getText().equals("")) {
                if (Double.parseDouble(jTextField8.getText()) >= Double.parseDouble(jLabel14.getText())) {

                    int r = JOptionPane.showConfirmDialog(this, "Are you sure do you want to complete this process", "Conform", JOptionPane.INFORMATION_MESSAGE);

                    if (r == 0) {

//                        select which transactio doing
                        String payment = String.valueOf(Double.parseDouble(jTextField8.getText()));

                        if (as != null) { // new student
                            System.out.println("addd");
                            this.dispose();
                            this.as.adding(payment);
                        } else if (us != null) { //update studenet class
                            System.out.println("update");
                            this.dispose();
                            this.us.adding(payment);
                        } else if (pmf != null) {  // student monthly fee
                            this.dispose();
                            System.out.println("payMonthlyFee");
                            this.pmf.pay(payment);
                        }

                    }

                } else {

                    JOptionPane.showMessageDialog(this, "Insufficient cash. Unable to proceed", "Warning", JOptionPane.WARNING_MESSAGE);

                }
            }

        } else if (evt.getKeyCode() == 27) {
            this.dispose();
        }
    }//GEN-LAST:event_jTextField8KeyReleased

