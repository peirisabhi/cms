    public ManageClass(Classes cl, String cId, String theme) {
        this.theme = theme;
        this.cl = cl;
        this.cId = cId;
        initComponents();
        teacherLoad();
        gradeLoad();
        themeLoad(theme);

        try {
            ResultSet rs = search("SELECT * FROM class INNER JOIN teacher INNER JOIN subject INNER JOIN grade INNER JOIN day WHERE class.subject_id = subject.id && class.teacher_id = teacher.id && class.day_id = day.id && subject.grade_id = grade.id && class.`status` = '1' && class.id = '" + cId + "'");

            if (rs.next()) {

                jTextField1.setText(cId);
                jComboBox1.setSelectedItem(rs.getString("name"));
                jComboBox2.setSelectedItem(rs.getString("grade"));
                subjectLoad(rs.getString("grade"));
                jComboBox3.setSelectedItem(rs.getString("subject"));
                jTextArea1.setText(rs.getString("desc"));
                jSpinner1.setValue(rs.getObject("start_time"));
                jSpinner2.setValue(rs.getTime("end_time"));
                jTextField13.setText(rs.getString("addmision"));
                jTextField11.setText(rs.getString("fee"));
                jComboBox4.setSelectedItem(rs.getString("day"));

            }

        } catch (Exception e) {
            System.out.println(e);
            warning(e);
        }

    }

