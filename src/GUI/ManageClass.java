    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id = jTextField1.getText();
        String teacher = jComboBox1.getSelectedItem().toString();
        String subject = jComboBox3.getSelectedItem().toString();
        String desc = jTextArea1.getText();
        Date starttime = (Date) jSpinner1.getValue();
        Date endtime = (Date) jSpinner2.getValue();
        String addmision = jTextField13.getText();
        String fee = jTextField11.getText();
        String day = jComboBox4.getSelectedItem().toString();

        SimpleDateFormat sdf = new SimpleDateFormat("hh:mm");
        String start = sdf.format(starttime);
        String end = sdf.format(endtime);

        if (!desc.equals("") && !addmision.equals("") && !fee.equals("")) {

            try {
                ResultSet rs1 = search("SELECT * from teacher WHERE name = '" + teacher + "'");
                ResultSet rs2 = search("SELECT * from subject WHERE subject = '" + subject + "'");
                ResultSet rs3 = search("SELECT * from day WHERE day = '" + day + "'");

                if (rs1.next()) {
                    String teacher_id = rs1.getString("id");
                    if (rs2.next()) {
                        String subject_id = rs2.getString("id");
                        if (rs3.next()) {
                            String day_id = rs3.getString("id");

                            if (1 == iud("INSERT INTO `class_managment`.`class` (`id`, `subject_id`, `teacher_id`, `day_id`, `desc`, `start_time`, `end_time`, `addmision`, `fee`, `status`)"
                                    + " VALUES ('" + id + "', '" + subject_id + "', '" + teacher_id + "', '" + day_id + "', '" + desc + "', '" + start + "', '" + end + "', '" + addmision + "', '" + fee + "', '1');")) {
                                JOptionPane.showMessageDialog(this, "Class Details are Saved", "success", JOptionPane.INFORMATION_MESSAGE);
                                this.cl.tableLoad();
                                this.dispose();
                            } else {
                                JOptionPane.showMessageDialog(this, "Can not add class", "Error", JOptionPane.ERROR_MESSAGE);
                                jTextArea1.setText(" ");
                                jComboBox1.setSelectedIndex(0);
                                jComboBox2.setSelectedIndex(0);
                                jComboBox3.setSelectedIndex(0);
                                jComboBox4.setSelectedIndex(0);
                                jTextField13.setText(" ");
                                jTextField13.setText(" ");
                            }

                        }
                    }
                }

            } catch (SQLException ex) {
                System.out.println(ex);
                warning(ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Required field missing", "warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

