    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        if (evt.getClickCount() == 2) {

            int row = jTable2.getSelectedRow();

            tId = jTable2.getValueAt(row, 0).toString();

            TableModel model = jTable1.getModel();
            DefaultTableModel dtm = (DefaultTableModel) model;
            dtm.setRowCount(0);

            try {
                ResultSet rs = search("SELECT * FROM seminar INNER JOIN subject INNER JOIN grade WHERE seminar.subject_id = subject.id && subject.grade_id = grade.id && seminar.teacher_id = '" + tId + "'");
                int i = 0;
                tot = 0.00;
                jLabel27.setText(String.valueOf(i));
                jLabel9.setText(String.valueOf(tot));
                while (rs.next()) {

                    String query1 = "SELECT * FROM seminar_payment_seminars WHERE seminar_id = '" + rs.getString("id") + "'";

                    ResultSet rs1 = search(query1);

                    if (rs1.next()) {
                        JOptionPane.showMessageDialog(null, jTable2.getValueAt(row, 1) + "'s "+"all seminar payments are setteled.", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {

                        String query2 = "SELECT student_class_card_type.`type` AS type,\n"
                                + "	COUNT(seminar_attendance.id) AS `students`,\n"
                                + "	seminar_attendance.paying AS `fee`\n"
                                + "      \n"
                                + "FROM \n"
                                + "	seminar_attendance\n"
                                + "	INNER JOIN student_class_card_type\n"
                                + "                                        \n"
                                + "WHERE \n"
                                + "	seminar_attendance.seminar_id = '" + rs.getString("id") + "' \n"
                                + "	&& seminar_attendance.`type` = student_class_card_type.id\n"
                                + "   \n"
                                + "GROUP BY \n"
                                + "	student_class_card_type.id\n"
                                + "   HAVING COUNT(student_class_card_type.id) >= 1";
                        
                        
                        ResultSet rs2 = search(query2);
                        
                        
                         while (rs2.next()) {
                                    i++;
                                    Vector<Object> v = new Vector<>();

                                    v.add(String.valueOf(i));
                                    v.add(rs.getString("seminar.id"));

                                    v.add(rs.getString("grade") +" "+rs.getString("subject") + "-" + rs.getString("desc") + " (" + rs.getString("date") + ")");
                                    v.add(rs2.getString("students") + " (" + rs2.getString("type") + ")");

                                    if (rs2.getString("type").equals("Full")) {
                                        v.add(rs2.getString("fee"));
                                        v.add(String.valueOf(Double.valueOf(rs2.getString("fee")) - Double.parseDouble(rs2.getString("fee")) * Double.parseDouble(institute.get("to_institute"))));
                                        v.add(String.valueOf((Double.valueOf(rs2.getString("fee")) - (Double.parseDouble(rs2.getString("fee")) * Double.parseDouble(institute.get("to_institute")))) * Double.parseDouble(rs2.getString("students"))));
                                        tot = tot + ((Double.valueOf(rs2.getString("fee")) - (Double.parseDouble(rs2.getString("fee")) * Double.parseDouble(institute.get("to_institute"))))) * Double.parseDouble(rs2.getString("students"));
                                    }
                                    if (rs2.getString("type").equals("Half")) {
                                        v.add(String.valueOf(Double.parseDouble(rs2.getString("fee"))));
                                        v.add(String.valueOf((Double.parseDouble(rs2.getString("fee"))) - ((Double.parseDouble(rs2.getString("fee"))) * Double.parseDouble(institute.get("to_institute")))));
                                        v.add(String.valueOf(((Double.parseDouble(rs2.getString("fee"))) - ((Double.parseDouble(rs2.getString("fee"))) * Double.parseDouble(institute.get("to_institute")))) * Double.parseDouble(rs2.getString("students"))));
                                        tot = tot + ((Double.parseDouble(rs2.getString("fee"))) - ((Double.parseDouble(rs2.getString("fee"))) * Double.parseDouble(institute.get("to_institute")))) * Double.parseDouble(rs2.getString("students"));
                                    }
                                    if (rs2.getString("type").equals("Free")) {
                                        v.add("0.00");
                                        v.add("0.00");
                                        v.add(String.valueOf(0 * Double.parseDouble(rs2.getString("students"))));
                                    }

                                    dtm.addRow(v);

//                        System.out.println(rs1.getString("fee"));
                                }
                                jLabel27.setText(String.valueOf(i));
                                jLabel9.setText(String.valueOf(tot));
                        

                    }

                    jLabel27.setText(String.valueOf(i));
                    jLabel9.setText(String.valueOf(tot));

                }

            } catch (Exception e) {
                System.out.println(e);
                warning(e);
            }

        }
    }//GEN-LAST:event_jTable2MouseClicked

