    void teacherLoad() {

        TableModel model = jTable2.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {

            String q = "SELECT\n"
                    + "	teacher.id,\n"
                    + "	teacher.name,\n"
                    + "	teacher.nic,\n"
                    + "	teacher.email,\n"
                    + "	teacher.contact1,\n"
                    + "	teacher.contact2\n"
                    + "\n"
                    + "FROM\n"
                    + "	seminar\n"
                    + "	INNER JOIN teacher\n"
                    + "	INNER JOIN subject\n"
                    + "	\n"
                    + "WHERE \n"
                    + "	seminar.subject_id = subject.id\n"
                    + "	&& seminar.teacher_id = teacher.id\n"
                    + "	&& teacher.`status` = '1'\n"
                    + "	&& seminar.`status` = '1'\n"
                    + "\n"
                    + "GROUP BY \n"
                    + "	teacher.id\n"
                    + "	HAVING COUNT(teacher.id) >= 1;";

            ResultSet rs = search(q);
            int i = 1;
            while (rs.next()) {
                Vector<String> v = new Vector<>();

                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("nic"));
                v.add(rs.getString("email"));
                v.add(rs.getString("contact1") + " / " + rs.getString("contact2"));

                dtm.addRow(v);

                jLabel26.setText(String.valueOf(i));
                i++;
            }

        } catch (Exception e) {
            System.out.println(e);
            warning(e);
        }

    }

