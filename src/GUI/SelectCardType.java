                v.add("0.00");
                v.add(this.cl.get("addmision"));
                v.add("Free-" + jTextArea1.getText());
                v.add(String.valueOf(Double.parseDouble(this.cl.get("addmision"))));

                dtm.addRow(v);
                this.dispose();

                this.as.jLabel27.setText(String.valueOf(this.as.count));
                this.as.tot = this.as.tot + Double.parseDouble(this.cl.get("addmision"));
                this.as.jLabel9.setText(String.valueOf(this.as.tot));

