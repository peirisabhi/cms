    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!jTextField1.getText().equals("") && !jTextArea1.getText().equals("")) {

            if (jTextField2.getText().equals("All Teachers")) {

                ResultSet rs = search("SELECT * FROM teacher WHERE status = '1'");
                mails(rs, jTextField1.getText(), jTextArea1.getText());
                sendMail.start();
                JOptionPane.showMessageDialog(this, "Email's are sending", "success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {

                sendMail(jTextField2.getText(), jTextField1.getText(), jTextArea1.getText());
                JOptionPane.showMessageDialog(this, "Email sent", "success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }

        } else {

            JOptionPane.showMessageDialog(this, "Required field missing", "warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

