    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            ResultSet rs = search("SELECT COUNT(id) AS count FROM school WHERE school = '" + jTextField1.getText() + "';");

            if (rs.next()) {

                if (rs.getString("count").equals("0")) {
                    if (1 == iud("INSERT INTO `class_managment`.`school` (`school`) VALUES ('" + jTextField1.getText() + "');")) {
                       
                        JOptionPane.showMessageDialog(this, "School added", "Success", JOptionPane.INFORMATION_MESSAGE);
                        if(as != null){
                            this.as.schoolLoad();
                        }else{
                            this.us.schoolLoad();
                        }
                        this.dispose();
                        
                    } else {
                        jTextField1.setText("");
                        JOptionPane.showMessageDialog(this, "Can not add this school now, please try again", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    jTextField1.setText("");
                    JOptionPane.showMessageDialog(this, "This school is already in system", "Warning", JOptionPane.WARNING_MESSAGE);
                }

            }

        } catch (Exception ex) {
            System.out.println(ex);
            warning(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

